function setLanguage(lang) {
    localStorage.setItem('preferredLang', lang);
    document.documentElement.lang = lang === 'pt' ? 'pt-br' : 'en';
    
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            // Handle markdown-like bold text in guide
            if (el.innerHTML.includes('**')) {
                el.innerHTML = translations[lang][key].replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>');
            } else {
                el.textContent = translations[lang][key];
            }
        }
    });

    // Update buttons style
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('text-yellow-300', 'font-bold');
            btn.classList.remove('opacity-50');
        } else {
            btn.classList.remove('text-yellow-300', 'font-bold');
            btn.classList.add('opacity-50');
        }
    });

    // Update special cases like toast
    const toast = document.getElementById('toast');
    if (toast) toast.textContent = translations[lang].toast_copy;

    // Show/Hide language specific blocks (for blog posts)
    document.querySelectorAll('[lang]').forEach(el => {
        if (el.tagName === 'HTML') return;
        if (el.getAttribute('lang') === lang) {
            el.style.display = '';
        } else {
            el.style.display = 'none';
        }
    });
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLang') || 'pt';
    setLanguage(savedLang);

    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            setLanguage(btn.getAttribute('data-lang'));
        });
    });
});
